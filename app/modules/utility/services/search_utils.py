"""Безопасные LIKE/ILIKE-паттерны из пользовательского ввода.

Запросы параметризованы (SQLi нет), но спецсимволы LIKE (`%`, `_`) из ввода
трактуются как метасимволы → wildcard-инъекция (over-match, обход фильтра) и
лишняя нагрузка. Убираем их из ввода: цели поиска (ФИО, № комнат, адреса) их
не содержат, удаление безвредно. Security-аудит 2026-06-09.
"""


def like_contains(value) -> str:
    """Безопасный паттерн «содержит» для .like()/.ilike(): %<ввод без wildcard>%.

    Использование: col.ilike(like_contains(search.lower()))
    """
    s = "" if value is None else str(value)
    s = s.replace("%", "").replace("_", "")
    return f"%{s}%"
