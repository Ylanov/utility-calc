"""Помощники для работы с биллинговыми периодами.

Главная проблема: `BillingPeriod.id` — это порядок создания записи в БД,
а не хронология биллингового месяца. Если админ задним числом импортирует
подачу за «Февраль 2026» в мае, новый period.id будет БОЛЬШЕ чем у уже
существующего «Май 2026». Сортировка по period.id → февраль показывается
выше мая, и дельты считаются от «не того» предыдущего показания.

Аналогично с `created_at` — он отражает физическое создание записи,
а не биллинговый месяц.

Эти helper'ы парсят русские названия месяцев в (year, month) для
хронологически правильной сортировки. Применяется в админ-аналитике
(/api/admin/residents/{id}/finance-detail) и везде где нужно
«найти предыдущий биллинговый период».
"""
from typing import Optional, Tuple

# Карта русских месяцев в номера. В именительном падеже — так пишутся
# в BillingPeriod.name (например «Май 2026»). Регистр игнорируется.
RUSSIAN_MONTH_TO_NUM: dict[str, int] = {
    "январь": 1,
    "февраль": 2,
    "март": 3,
    "апрель": 4,
    "май": 5,
    "июнь": 6,
    "июль": 7,
    "август": 8,
    "сентябрь": 9,
    "октябрь": 10,
    "ноябрь": 11,
    "декабрь": 12,
}


def parse_period_name(name: Optional[str]) -> Optional[Tuple[int, int]]:
    """Парсит «Май 2026» → (2026, 5).

    Возвращает None для нестандартных имён («Начальный период», пустых
    строк, тестовых имён). Такие сортируются отдельно — обычно как baseline
    в самом начале хронологии.

    Год должен быть в диапазоне 2000..2100 — защита от опечаток вроде
    «Май 20226». Месяц 1..12.
    """
    if not name:
        return None
    parts = name.strip().lower().split()
    if len(parts) != 2:
        return None
    month = RUSSIAN_MONTH_TO_NUM.get(parts[0])
    if month is None:
        return None
    try:
        year = int(parts[1])
    except ValueError:
        return None
    if year < 2000 or year > 2100:
        return None
    return (year, month)


def period_chron_key(name: Optional[str]) -> Tuple[int, int]:
    """Ключ хронологической сортировки.

    «Начальный период» / непарсимые имена → `(0, 0)` — попадают
    в самое начало (как baseline до всякой реальной истории).
    Обычные периоды — `(year, month)`, сортируются естественным образом.
    """
    parsed = parse_period_name(name)
    return parsed if parsed else (0, 0)
